# imports
from pydantic import BaseModel
from typing import Optional
import pandas as pd

# DatasetSummary
class DatasetSummary(BaseModel):
    row_count: int
    column_count: int                                                    # Stores the metadata extracted from the dataset.
    columns: list[str]                                                   # Used by the Planner to understand the dataset without sending the entire DataFrame to the LLM.
    data_types: dict[str, str]
    numeric_columns: list[str]
    categorical_columns:list
    missing_values: dict[str, int]
    sample_data: dict
    dataset_description: str

# AnalysisPlan
class AnalysisTask(BaseModel):
    analysis_name: str                                                    # Represents one analysis task decided by the Planner.
    description: str                                                      # Defines what analysis to perform and what dataset columns are required.
    required_columns: list[str]

class AnalysisPlan(BaseModel):                                            # Contains the complete execution plan generated by the Planner.
    tasks: list[AnalysisTask]                                             # Acts as the contract between the Planner and the Analyzer.

# AnalysisResults
class AnalysisResult(BaseModel):                                          # Stores the output of one completed analysis.
    analysis_name: str                                                    # Keeps analysis results structured so downstream nodes can consume them reliably.
    result: dict

class AnalysisResults(BaseModel):                                         # Collects the outputs of all executed analyses.
    analyses: list[AnalysisResult]                                        # Serves as the single source of truth for the Visualizer and Insight Generator.

# BusinessInsights
class BusinessInsight(BaseModel):                                
    title: str                                                            # Represents one business insight generated from the analysis results.
    description: str                                                      # Maintains traceability by linking each insight to its source analysis.
    source_analysis: str

class BusinessInsights(BaseModel):                                        # Collects all business insights generated by the Insight Generator.
    insights: list[BusinessInsight]                                       # Used by the Insight Reviewer and Report Generator.


# GraphState
class GraphState(BaseModel):                                              # Shared state passed between all LangGraph nodes.
    user_query: str                                                       # Stores the output of each node so the workflow can evolve step by step.
    csv_path: str

    dataframe: pd.DataFrame
    dataset_summary: Optional[DatasetSummary] = None
    analysis_plan: Optional[AnalysisPlan] = None
    analysis_results: Optional[AnalysisResults] = None
    business_insights: Optional[BusinessInsights] = None
    chart_paths: Optional[list[str]] = None
    final_report: Optional[str] = None
    error: Optional[str] = None